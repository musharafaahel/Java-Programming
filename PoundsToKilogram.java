package com.company;

public class PoundsToKilogram {
    public static void main(String[] args) {
        double pound = 200.0;
        double kilogram = 0.45359237;
        // 1 pound  = 0.45359237 kilogram
        double result = pound * kilogram;
        System.out.println(pound + " Pounds = "+ result + " Kilogram ");

    }
}
