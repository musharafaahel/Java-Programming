package com.company;

public class OperatorsAndOperands {
    public static void main(String[] args) {
        int num1 = 55;
        int num2 = 10;

        System.out.println("num1 + num2 = "+ (num1 + num2));
        System.out.println("num1 - num2 = "+ (num1 - num2));
        System.out.println("num1 * num2 = "+ (num1 * num2));
        System.out.println("num1 / num2 = "+ (num1 / num2));
        System.out.println("num1 % num2 = "+ (num1 % num2));
    }
}
